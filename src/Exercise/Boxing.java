package Exercise;

import java.util.ArrayList;

public class Boxing {
    public static void main(String[] args) {


        //=> Boxing
        Integer inte = new Integer (10) ;

        System.out.println(inte);


        System.out.println("------------");

        //=> Unboxing
        int one = inte;
        System.out.println(one);


        Character cha = ('B');
        System.out.println(cha);

        char charr = cha;
        System.out.println(charr);

    }
}
